#pragma once
#include <iostream>
using namespace std;
class Fraction {
private:
	int n;
	int d;
public:
	Fraction();
	Fraction(int);
	Fraction(int, int);
	~Fraction();
	Fraction operator + (const Fraction&)const;
	Fraction operator - (const Fraction&)const;
	Fraction operator * (const Fraction&)const;
	Fraction operator / (const Fraction&)const;
	Fraction operator = (const Fraction&);
	friend istream& operator >> (istream& ist, Fraction& ft);
	friend ostream& operator << (ostream& ost, const Fraction& ft);
};

